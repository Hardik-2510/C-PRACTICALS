#include<iostream>
using namespace std;

int main(){

     // Check Odd Even Numbers
     
     int n ;
     cout << "Enter a number :- " ;
     cin >> n ;

     if (n % 2 == 0)
     {
          cout << n << " is Even Number ." ;
     }
     else{
          cout << n << " is Odd Number ." ;
     }
     
}
