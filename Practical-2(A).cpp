#include<iostream>
using namespace std;

int main(){

     // design basic calculator 

     float x , y ;
     cout << "Enter Number-1 :- ";
     cin >> x ;
     cout << "Enter Number-2 :- ";
     cin >> y ;

     cout << x << " + " << y << " = " << (x + y) << endl;
     cout << x << " - " << y << " = " << (x - y) << endl;
     cout << x << " * " << y << " = " << (x * y) << endl;
     cout << x << " / " << y << " = " << (x / y) << endl;

}
